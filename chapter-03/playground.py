# how many seconds 
seconds_in_a_day = 24 * 60 * 60
seconds_in_a_month = seconds_in_a_day * 30
seconds_in_a_year = seconds_in_a_day * 365
seconds_in_100_years = seconds_in_a_year * 100
seconds_in_138_billion_years = seconds_in_a_year * 138 * (10 ** 9)
print(f"{seconds_in_a_day=: .2e}")
print(f"{seconds_in_a_month=: .2e}")
print(f"{seconds_in_a_year=: .2e}")
print(f"{seconds_in_100_years=: .2e}")
print(f"{seconds_in_138_billion_years=: .2e}")


